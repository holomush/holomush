// Command gen emits internal/plugin/luabridge/bindings_gen.go: the typed Lua
// bindings for the holomush.plugin.host.v1 capability services.
//
// For each host-capability token in plugins.CapabilityServiceNames (the
// single-source token↔service map) it resolves the backing service descriptor
// from the registered host.v1 FileDescriptors and emits a register<Svc>
// function. Each registrar builds a Lua table whose methods marshal the Lua
// argument into the typed request, invoke the generated client over the
// supplied conn, and marshal the typed response back into a Lua table. It also
// emits registeredHostCapBindings, keyed by CAPABILITY TOKEN (not service name),
// because the manifest gate is RequiredCapabilities().
//
// Usage:
//
//	go generate ./internal/plugin/luabridge/
//	# or directly:
//	go run ./internal/plugin/luabridge/gen
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"os"
	"path/filepath"
	"sort"
	"text/template"

	"google.golang.org/protobuf/reflect/protoreflect"
	"google.golang.org/protobuf/reflect/protoregistry"

	plugins "github.com/holomush/holomush/internal/plugin"

	// Imported for its side effect: registering the host.v1 FileDescriptors in
	// protoregistry.GlobalFiles so the generator can introspect the services.
	_ "github.com/holomush/holomush/pkg/proto/holomush/plugin/host/v1"
)

const hostV1Package = "holomush.plugin.host.v1"

// bindingsTmpl renders the full bindings_gen.go file: per-service registrars
// plus the token-keyed dispatch map.
const bindingsTmpl = `// Code generated by luabridge/gen. DO NOT EDIT.

// SPDX-License-Identifier: Apache-2.0
// Copyright 2026 HoloMUSH Contributors

//nolint:gocritic // captLocal: L is the idiomatic name for lua.LState.
package luabridge

import (
	lua "github.com/yuin/gopher-lua"
	"google.golang.org/grpc"

	hostv1 "github.com/holomush/holomush/pkg/proto/holomush/plugin/host/v1"
)
{{range .Services}}
// register{{.GoName}} injects the "{{.Token}}" host-capability namespace (backed
// by {{.ServiceName}}) as a global Lua table on L, dispatching each method over
// conn. pluginName is reserved for per-plugin scoping by the caller.
func register{{.GoName}}(L *lua.LState, conn grpc.ClientConnInterface, pluginName string) {
	_ = pluginName
	tbl := L.NewTable()
	client := hostv1.New{{.GoName}}Client(conn)
{{range .Methods}}	L.SetField(tbl, "{{.GoName}}", L.NewFunction(func(L *lua.LState) int {
		var req hostv1.{{.RequestGoType}}
		if err := LuaTableToProto(L.CheckTable(1), &req); err != nil {
			return pushBridgeError(L, err)
		}
		resp, err := client.{{.GoName}}(luaContext(L), &req)
		if err != nil {
			return pushBridgeError(L, err)
		}
		L.Push(ProtoToLuaTable(L, resp))
		L.Push(lua.LNil)
		return 2
	}))
{{end}}	L.SetGlobal("{{.Token}}", tbl)
}
{{end}}
// registeredHostCapBindings maps CAPABILITY TOKEN (the vocab token a manifest
// declares as ` + "`requires: - capability: <token>`" + `) to its registrar. It is
// keyed by token, NOT service name, because the manifest gate is
// RequiredCapabilities() (capability kind), not RequiredServiceNames() (service
// kind). The token↔service mapping is plugins.CapabilityServiceNames.
var registeredHostCapBindings = map[string]func(*lua.LState, grpc.ClientConnInterface, string){
{{range .Services}}	"{{.Token}}": register{{.GoName}},
{{end}}}
`

// serviceData is the template input for one host-capability service.
type serviceData struct {
	Token       string
	GoName      string // e.g. KVService
	ServiceName string // fully-qualified, e.g. holomush.plugin.host.v1.KVService
	Methods     []methodData
}

// methodData is the template input for one unary RPC.
type methodData struct {
	GoName         string // e.g. Get
	RequestGoType  string // e.g. GetRequest
	ResponseGoType string // e.g. GetResponse
}

func main() {
	services, err := collectServices()
	if err != nil {
		log.Fatalf("collecting host.v1 services: %v", err)
	}

	tmpl := template.Must(template.New("bindings").Parse(bindingsTmpl))
	var buf bytes.Buffer
	if execErr := tmpl.Execute(&buf, map[string]any{"Services": services}); execErr != nil {
		log.Fatalf("executing template: %v", execErr)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("formatting generated source: %v\n%s", err, buf.String())
	}

	root := findModuleRoot()
	outPath := filepath.Join(root, "internal", "plugin", "luabridge", "bindings_gen.go")
	if wErr := os.WriteFile(outPath, formatted, 0o600); wErr != nil {
		log.Fatalf("writing %s: %v", outPath, wErr)
	}
	fmt.Printf("wrote %s (%d services)\n", outPath, len(services))

	// Second output: the LuaLS editor stub (holomush-eykuh.9). Off the runtime
	// path — purely for editor autocomplete/hover.
	messages, err := collectStubMessages(services)
	if err != nil {
		log.Fatalf("collecting stub messages: %v", err)
	}
	stub, err := renderLuaStub(services, messages, ambientDecls)
	if err != nil {
		log.Fatalf("rendering lua stub: %v", err)
	}
	stubPath := filepath.Join(root, "pkg", "plugin", "luastubs", "holomush.lua")
	if mkErr := os.MkdirAll(filepath.Dir(stubPath), 0o750); mkErr != nil {
		log.Fatalf("creating luastubs dir: %v", mkErr)
	}
	if wErr := os.WriteFile(stubPath, []byte(stub), 0o600); wErr != nil {
		log.Fatalf("writing %s: %v", stubPath, wErr)
	}
	fmt.Printf("wrote %s\n", stubPath)
}

// collectServices resolves every token in plugins.CapabilityServiceNames to its
// registered host.v1 service descriptor and gathers its unary methods. It
// returns services sorted by token for deterministic output.
func collectServices() ([]serviceData, error) {
	tokens := make([]string, 0, len(plugins.CapabilityServiceNames))
	for token := range plugins.CapabilityServiceNames {
		tokens = append(tokens, token)
	}
	sort.Strings(tokens)

	out := make([]serviceData, 0, len(tokens))
	for _, token := range tokens {
		goName := plugins.CapabilityServiceNames[token]
		fullName := protoreflect.FullName(hostV1Package + "." + goName)
		desc, err := protoregistry.GlobalFiles.FindDescriptorByName(fullName)
		if err != nil {
			return nil, fmt.Errorf("token %q: service %q not registered: %w", token, fullName, err)
		}
		sd, ok := desc.(protoreflect.ServiceDescriptor)
		if !ok {
			return nil, fmt.Errorf("token %q: %q is not a service descriptor", token, fullName)
		}

		methods, err := collectMethods(token, sd)
		if err != nil {
			return nil, err
		}
		out = append(out, serviceData{
			Token:       token,
			GoName:      goName,
			ServiceName: string(fullName),
			Methods:     methods,
		})
	}
	return out, nil
}

// collectMethods gathers the unary RPCs of sd. Streaming RPCs are skipped (the
// unary client.Method(ctx, req) shape the bindings use does not apply); none
// exist in host.v1 today, but the guard keeps the generator correct if one is
// added.
func collectMethods(token string, sd protoreflect.ServiceDescriptor) ([]methodData, error) {
	methods := sd.Methods()
	out := make([]methodData, 0, methods.Len())
	for i := 0; i < methods.Len(); i++ {
		md := methods.Get(i)
		if md.IsStreamingClient() || md.IsStreamingServer() {
			continue
		}
		reqGoType := goTypeName(md.Input().Name())
		out = append(out, methodData{
			GoName:         string(md.Name()),
			RequestGoType:  reqGoType,
			ResponseGoType: goTypeName(md.Output().Name()),
		})
	}
	if len(out) == 0 {
		return nil, fmt.Errorf("token %q: service %q has no unary methods", token, sd.FullName())
	}
	return out, nil
}

// goTypeName returns the Go type name for a host.v1 message. protoc-gen-go names
// the Go type after the message's proto name with the PascalCase preserved, so
// the descriptor name is the Go type name directly (e.g. GetPropertyRequest).
func goTypeName(name protoreflect.Name) string {
	return string(name)
}

// findModuleRoot walks up from the working directory to the directory holding
// go.mod, the module root.
func findModuleRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		log.Fatalf("getwd: %v", err)
	}
	for {
		if _, statErr := os.Stat(filepath.Join(dir, "go.mod")); statErr == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			log.Fatal("could not find module root (go.mod) from " + dir)
		}
		dir = parent
	}
}
