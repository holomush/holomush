package eventbus

import (
	"context"

	"github.com/oklog/ulid/v2"

	"github.com/holomush/holomush/internal/eventbus/codec"
)

// IdentityKind identifies the type of principal in a session or history request.
// Mirrors authguard.IdentityKind — kept in sync by the bridge in
// internal/eventbus/authguard/adapter_identity.go.
type IdentityKind int

// IdentityKind constants enumerate the principals AuthGuard can evaluate.
const (
	IdentityKindUnknown   IdentityKind = iota
	IdentityKindCharacter              // player character session
	IdentityKindPlayer                 // player reading own history
	IdentityKindPlugin                 // plugin with a declared grant
	IdentityKindOperator               // operator — always denied on Subscribe; uses AdminReadStream
)

// SessionIdentity is the typed authenticated principal attached to an
// eventbus.OpenSession or HistoryQuery call. It is a local copy of
// authguard.Identity, kept here to avoid an import cycle:
//
//	eventbus → authguard → plugin → eventbus
//
// Callers that have an authguard.Identity convert via
// authguard.ToSessionIdentity (generated by adapter_identity.go in Phase
// 3b T9). Zero value is safe for Crypto.Enabled=false deployments.
type SessionIdentity struct {
	Kind        IdentityKind
	PlayerID    string
	CharacterID string
	BindingID   string
	PluginName  string
	InstanceID  string
}

// SessionDecision is the result of a SessionAuthGuard.Check call.
type SessionDecision struct {
	Permit  bool
	GrantID ulid.ULID
}

// SessionCheckRequest is the input to SessionAuthGuard.Check.
type SessionCheckRequest struct {
	Identity   SessionIdentity
	KeyID      codec.KeyID
	KeyVersion uint32
	EventType  string
	EventID    ulid.ULID
	// ReadBack selects the read-back authorization path (manifest
	// crypto.emits[].readback) over the live-delivery path. Only meaningful
	// for IdentityKindPlugin. Mirrors authguard.CheckRequest.ReadBack.
	ReadBack bool
}

// SessionAuthGuard evaluates whether a principal may receive a sensitive event.
// Implemented by authguard.Guard (via authguard.BridgeGuard adapter). Nil
// means no auth evaluation — all sensitive events are delivered as-is (legacy
// passthrough; used when Crypto.Enabled=false).
type SessionAuthGuard interface {
	Check(ctx context.Context, req SessionCheckRequest) (SessionDecision, error)
}

// SessionDEKManager resolves DEK key material for decryption.
// Implemented by dek.Manager.
type SessionDEKManager interface {
	Resolve(ctx context.Context, keyID codec.KeyID, version uint32) (codec.Key, error)
}

// PluginDecryptRecord carries the fields logged when a plugin successfully
// receives a decrypted event payload. Mirrors audit.PluginDecryptRecord —
// kept here to avoid importing authguard/audit (which imports eventbus).
type PluginDecryptRecord struct {
	PluginName       string
	PluginInstanceID string
	EventID          ulid.ULID
	EventSubject     Subject
	EventType        Type
	DEKRef           codec.KeyID
	DEKVersion       uint32
	GrantID          ulid.ULID
}

// SessionAuditEmitter records plugin decrypt events for audit.
// Implemented by audit.Emitter (via adapter in the wiring layer).
// Nil is safe when no plugin sessions are expected.
type SessionAuditEmitter interface {
	EmitPluginDecrypt(ctx context.Context, rec PluginDecryptRecord) error
}
